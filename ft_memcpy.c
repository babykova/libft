#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char *s;
	char *d;

	s = (char *)dst;
	d = (char *)src;
	while (n > 0)
	{
		*s = *d;
		s++;
		d++;
		n--;
	}
	return (dst);
}
